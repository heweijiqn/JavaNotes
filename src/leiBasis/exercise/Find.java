package leiBasis.exercise;

public class Find {
    //编写类Home_2，定义方法find，实现查找某字符串是否在字符串数组中，
    //并返回索引，如果找不到，返回-1
    //分析
    //1. 类名 Home_2
    //2. 方法名 find
    //3. 返回值 int
    //4. 形参 (String , String[])

    public int find(String findStr, String[] strs) {
        //直接遍历字符串数组，如果找到，则返回索引
        for(int i = 0; i < strs.length; i++) {
            if(findStr.equals(strs[i])) {
                return i;
            }
        }
        //如果没有，就返回-1
        return -1;
    }

    public static void main(String[] ages){
        Find i = new Find();
        String[] strs = {"hwj", "abc", "hwi"};
        double index = i.find("hwi",strs);
        System.out.println("查找的index值是"+index);
    }
}
